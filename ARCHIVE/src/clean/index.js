function trainBPE(corpus, separators, merges, vocab, inverse) {
    const chunked = lib.chunk(corpus, separators);
    const initialLen = Object.entries(vocab).length;

    let totalMerges = 0;
    for(let i = 0; i < merges; i++) {
        const tokens = lib.encode(chunked, vocab);
        const freqs = {};

        for(let t = 0; t < tokens.length; t++) {
            lib.pfreq(tokens[t], freqs);
        }
        const pair = lib.maxfreq(freqs);
        if(!pair) break;
        
        lib.register(vocab, inverse, pair);
        totalMerges++;
    }

    return {
        merges: totalMerges,
        learned: Object.entries(vocab).slice(initialLen),
    }
}
function tokenize(src, separators, vocab) {
    const chunked = lib.chunk(src, separators);
    const tokens = [];

    for(let i = 0; i < chunked.length; i++) {
        for(let j = chunked[i].length; j > 0; j--) {
            const encoded = vocab[chunked[i].slice(0, j)];
            if(encoded !== undefined) {
                tokens.push(encoded);
                if(j !== 0) {
                    chunked[i] = chunked[i].slice(j);
                    i--;
                }

                break;
            }
        }
    }

    return tokens;
}

